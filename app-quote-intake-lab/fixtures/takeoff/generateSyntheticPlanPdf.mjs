function main() {
  const bytes = buildSyntheticKitchenIslandPdfBytes();
  const hash = sha256Hex(bytes);
  const pdfPath = join(__dirname, PDF_NAME);
  const gtPath = join(__dirname, GT_NAME);
  writeFileSync(pdfPath, bytes);
  writeFileSync(gtPath, JSON.stringify(buildGroundTruth(hash, bytes.length), null, 2) + "\n");
  console.log(`[qil-synth-plan] wrote ${PDF_NAME} (${bytes.length} bytes)`);
  console.log(`[qil-synth-plan] sha256=${hash}`);
  console.log(`[qil-synth-plan] wrote ${GT_NAME}`);
  // Soft open check: %PDF header
  const head = bytes.subarray(0, 5).toString("utf8");
  if (head !== "%PDF-") throw new Error("Generated file missing %PDF- header");
  if (existsSync(gtPath)) {
    const gt = JSON.parse(readFileSync(gtPath, "utf8"));
    if (gt.contentHash !== hash) throw new Error("Ground-truth hash mismatch after write");
  }
}

const isMain =
  process.argv[1] &&
  fileURLToPath(import.meta.url) === process.argv[1];
if (isMain) main();
